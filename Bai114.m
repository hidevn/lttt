A = [2 7 9 7 ; 3 1 5 6 ; 8 1 2 5];
fprintf('1.14.1: A'' = \n');
disp(A');
fprintf('1.14.2: sum(A) = \n');
disp(sum(A));
fprintf('1.14.3: sum(A'') = \n');
disp(sum(A'));
fprintf('1.14.4: sum(A, 2) = \n');
disp(sum(A, 2));
fprintf('1.14.5: [[A; sum(A)] [sum(A, 2); sum(A(:))]] = \n');
disp([[A; sum(A)] [sum(A, 2); sum(A(:))]]);
